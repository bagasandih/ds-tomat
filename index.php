              <p> Sanitasi kebun yang baik sangat penting untuk mencegah penyebaran penyakit. Singkirkan daun dan buah yang jatuh serta tanaman yang sakit dari kebun secara teratur. Alat berkebun harus dibersihkan dan didesinfeksi setelah digunakan, terutama jika telah digunakan pada tanaman yang terinfeksi penyakit.</p>
