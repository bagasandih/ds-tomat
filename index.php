              <h3>Pemilihan Varietas Tahan Penyakit</h3>
              <p>
                Memilih varietas tomat yang tahan terhadap penyakit tertentu adalah langkah pertama yang penting. Beberapa varietas tomat telah dikembangkan untuk memiliki resistensi terhadap penyakit seperti layu fusarium, layu bakteri, dan busuk daun. Informasi tentang resistensi penyakit biasanya tersedia pada label benih atau dari penjual benih.
              </p>
            </div>
          </div>

