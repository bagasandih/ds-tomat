            </div>
            <div class="col-md-7 pt-5" data-aos="fade-left">
              <h3>Penggunaan Media Tanam yang Steril</h3>
              <p> Menggunakan media tanam yang steril atau tanah baru dapat mengurangi risiko infeksi patogen tanah. Jika menggunakan tanah dari kebun, pastikan untuk memperbaiki struktur tanah dengan kompos dan memastikan drainase yang baik untuk menghindari genangan air yang dapat memicu perkembangan jamur.</p>
            </div>
          </div>

          <div class="row content">
