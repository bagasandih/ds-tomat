            </div>
            <div class="col-md-7 pt-5 order-2 order-md-1" data-aos="fade-right">
              <h3>Rotasi Tanaman</h3>
              <p>
                 Rotasi tanaman adalah praktik menghindari menanam tanaman tomat atau tanaman sejenis di tempat yang sama selama beberapa musim berturut-turut. Ini membantu mengurangi akumulasi patogen di dalam tanah yang dapat menyebabkan penyakit. Idealnya, tanaman dari keluarga yang berbeda sebaiknya ditanam di area yang sama setelah tomat untuk memutus siklus hidup patogen.
              </p>
            </div>
          </div>

          <div class="row content">
