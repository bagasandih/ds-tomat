              <h3>Penyiraman yang Tepat</h3>
              <p>
                Penyiraman yang tepat sangat penting untuk mencegah penyakit. Penyiraman sebaiknya dilakukan di pangkal tanaman dan menghindari penyiraman dari atas yang dapat membasahi daun. Daun yang basah dapat menjadi tempat berkembang biak bagi banyak patogen. Irigasi tetes adalah metode yang baik untuk memastikan penyiraman yang efisien dan mengurangi risiko penyakit.
              </p>
            </div>
          </div>

